-- Colonne transport_type sur bases héritées (nullable ou absente) + backfill (équ. TransportTypeDefaultMigration)
DO $tr$
BEGIN
  IF EXISTS (SELECT 1 FROM information_schema.tables
             WHERE table_schema = current_schema() AND table_name = 'trips') THEN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = current_schema() AND table_name = 'trips' AND column_name = 'transport_type') THEN
      ALTER TABLE trips ADD COLUMN transport_type varchar(32) DEFAULT 'PUBLIC';
    END IF;
    UPDATE trips SET transport_type = 'PUBLIC' WHERE transport_type IS NULL;
    ALTER TABLE trips ALTER COLUMN transport_type SET DEFAULT 'PUBLIC';
    IF NOT EXISTS (SELECT 1 FROM trips WHERE transport_type IS NULL) THEN
      ALTER TABLE trips ALTER COLUMN transport_type SET NOT NULL;
    END IF;
  END IF;
END
$tr$;

-- Contraintes CHECK (alignement enums Java) — s’applique aux bases existantes dès l’arrivée de V2
DO $chk1$
BEGIN
  IF EXISTS (SELECT 1 FROM information_schema.tables
             WHERE table_schema = current_schema() AND table_name = 'trips') THEN
    ALTER TABLE trips DROP CONSTRAINT IF EXISTS trips_vehicle_type_check;
    ALTER TABLE trips
      ADD CONSTRAINT trips_vehicle_type_check
      CHECK (vehicle_type IN (
        'BUS_CLIMATISE', 'BUS_CLASSIQUE', 'CAR_70_PLACES', 'MINIBUS',
        'MASSA_NORMAL', 'MASSA_6_ROUES', 'VAN',
        'SUV', 'BERLINE', 'CITADINE', 'MONOSPACE', 'PICKUP'));
  END IF;
END
$chk1$;

DO $chk2$
BEGIN
  IF EXISTS (SELECT 1 FROM information_schema.tables
             WHERE table_schema = current_schema() AND table_name = 'mobili_inbox_notifications') THEN
    ALTER TABLE mobili_inbox_notifications DROP CONSTRAINT IF EXISTS mobili_inbox_notifications_type_check;
    ALTER TABLE mobili_inbox_notifications
      ADD CONSTRAINT mobili_inbox_notifications_type_check
      CHECK (type IN (
        'TICKET_ISSUED', 'TRIP_CHANNEL_MESSAGE', 'PARTNER_NEW_BOOKING',
        'GARE_STATION_NEW_BOOKING', 'PARTNER_GARE_COM_MESSAGE',
        'COV_KYC_EXPIRING_SOON', 'COV_KYC_EXPIRED'));
  END IF;
END
$chk2$;
