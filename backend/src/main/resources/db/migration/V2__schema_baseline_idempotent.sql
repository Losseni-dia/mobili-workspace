-- Schéma de référence Mobili (idempotent, sans effacement de données).
-- Sur une base déjà remplie : CREATE IF NOT EXISTS ne fait rien (tables déjà là).
-- Les blocs DO en fin de fichier ajustent colonnes / contraintes sur des bases héritées d’Hibernate.
-- Règles de nommage : Spring PhysicalNaming (camelCase -> snake_case) sauf noms explicites @Column.

-- --- Noyau rôles / utilisateurs / partenaires / gares --------------------------------------------------
CREATE TABLE IF NOT EXISTS roles (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at    timestamp NOT NULL,
  name          varchar(255) NOT NULL UNIQUE
);

