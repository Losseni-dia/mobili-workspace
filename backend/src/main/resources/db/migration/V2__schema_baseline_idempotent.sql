  avatar_url                          varchar(255),
  enabled                             boolean NOT NULL DEFAULT true,
  balance                             double precision DEFAULT 0.0,
  station_id                          bigint,
  covoiturage_id_front_url            varchar(255),
  covoiturage_id_back_url            varchar(255),
  covoiturage_id_valid_until          date,
  covoiturage_kyc_status              varchar(32),
  covoiturage_vehicle_brand           varchar(255),
  covoiturage_vehicle_plate           varchar(255),
  covoiturage_vehicle_color           varchar(255),
  covoiturage_grey_card_number        varchar(255),
  covoiturage_vehicle_photo_url       varchar(255),
