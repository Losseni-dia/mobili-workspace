CREATE TABLE IF NOT EXISTS stations (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at         timestamp NOT NULL,
  name               varchar(255) NOT NULL,
  city               varchar(255) NOT NULL,
  code               varchar(255),
  approval_status    varchar(20),
  active             boolean NOT NULL DEFAULT true,
  validated          boolean,
  partner_id         bigint NOT NULL,
  CONSTRAINT fk_stations_partner FOREIGN KEY (partner_id) REFERENCES partners (id)
);

DO $c$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_users_station') THEN
    IF EXISTS (SELECT 1 FROM information_schema.tables
               WHERE table_schema = current_schema() AND table_name = 'stations')
       AND EXISTS (SELECT 1 FROM information_schema.columns
                  WHERE table_schema = current_schema() AND table_name = 'users' AND column_name = 'station_id') THEN
      ALTER TABLE users
        ADD CONSTRAINT fk_users_station FOREIGN KEY (station_id) REFERENCES stations (id);
    END IF;
  END IF;
END
$c$;

-- --- Trajets et segments -----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS trips (
  id                        bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at                timestamp NOT NULL,
  departure_city            varchar(255) NOT NULL,
  arrival_city              varchar(255) NOT NULL,
  boarding_point            varchar(255),
  vehicle_plate_number      varchar(255) NOT NULL,
  vehicle_image_url         varchar(255),
  departure_date_time       timestamp NOT NULL,
  price                     double precision NOT NULL,
  origin_destination_price  double precision,
  total_seats               integer NOT NULL,
  available_seats           integer NOT NULL,
  status                    varchar(255),
  stops_cities              text,
  vehicle_type              varchar(40) NOT NULL,
  transport_type            varchar(32) NOT NULL DEFAULT 'PUBLIC',
  partner_id                bigint NOT NULL,
  station_id                bigint,
  covoiturage_organizer_id  bigint,
